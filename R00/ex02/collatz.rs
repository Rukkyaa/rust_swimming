fn	collatz(start: u32)
{
	let mut	i = start;
	
	println!("{}", i);
	while i != 1
	{
		i = [i / 2, 3 * i + 1][(i % 2) as usize];
		println!("{}", i);
	}
}

fn	main()
{
	collatz(3);
}